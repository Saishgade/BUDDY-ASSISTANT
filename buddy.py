def _load_system_prompt() -> str:
    try:
        return PROMPT_PATH.read_text(encoding="utf-8")
    except Exception:
        return (
            "You are BUDDY, a helpful AI assistant. "
            "Be concise, direct, and always use the provided tools to complete tasks. "
            "Never simulate or guess results — always call the appropriate tool."
        )


def _get_welcome_message() -> str:
    try:
        data = json.loads(API_CONFIG_PATH.read_text(encoding="utf-8"))
        return data.get("welcome_message", DEFAULT_WELCOME_SPEECH) or DEFAULT_WELCOME_SPEECH
    except Exception:
        return DEFAULT_WELCOME_SPEECH


def _get_voice_name() -> str:
    try:
        data = json.loads(API_CONFIG_PATH.read_text(encoding="utf-8"))
        return data.get("voice_name", DEFAULT_VOICE_NAME) or DEFAULT_VOICE_NAME
    except Exception:
        return DEFAULT_VOICE_NAME


def _set_voice_name(name: str) -> None:
    try:
        data = json.loads(API_CONFIG_PATH.read_text(encoding="utf-8"))
    except Exception:
        data = {}
    data["voice_name"] = name
    try:
        API_CONFIG_PATH.write_text(json.dumps(data, indent=4), encoding="utf-8")
    except Exception as exc:
        print(f"[BUDDY] ⚠️ Failed to save voice_name: {exc}")


_last_memory_input = ""


def _update_memory_async(user_text: str, buddy_text: str) -> None:
    global _last_memory_input

    user_text   = (user_text   or "").strip()
    buddy_text  = (buddy_text or "").strip()

    if len(user_text) < 5 or user_text == _last_memory_input:
        return
    _last_memory_input = user_text

    try:
        api_key = _get_api_key()
        if not should_extract_memory(user_text, buddy_text, api_key):
            return
        data = extract_memory(user_text, buddy_text, api_key)
        if data:
            update_memory(data)
            print(f"[Memory] ✅ {list(data.keys())}")
    except Exception as e:
        if "429" not in str(e):
            print(f"[Memory] ⚠️ {e}")


