        os.makedirs(CONFIG_DIR, exist_ok=True)
        config = {
            "gemini_api_key":     key,
            "openrouter_api_key": or_key,
            "os_system":          os_name,
        }
        if welcome_message:
            config["welcome_message"] = welcome_message
        if voice_name:
            config["voice_name"] = voice_name

        API_CONFIG_PATH.write_text(
            json.dumps(config, indent=4),
            encoding="utf-8",
        )
        self._ready = True
        if self._overlay:
            self._overlay.hide()
            self._overlay = None
        self._apply_state("LISTENING")
        self._log.append_log(f"SYS: Initialised. OS={os_name.upper()}. BUDDY online.")

